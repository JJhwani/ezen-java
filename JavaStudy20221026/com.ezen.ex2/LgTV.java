package com.ezen.ex02;

public class LgTV {
	public void turnOn() {
		System.out.println("LgTv 전원 켠다.");
	}
	public void turnOff() {
		System.out.println("LgTv 전원 끈다.");
	}
	public void soundUp() {
		System.out.println("LgTv 소리를 높인다.");
	}
	public void soundDown() {
		System.out.println("LgTv 소리를 내린다.");
	}
}